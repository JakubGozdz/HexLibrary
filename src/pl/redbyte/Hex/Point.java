package pl.redbyte.Hex;

public class Point {
    /**
     * The x, y coords of the simple Point object
     */
    public final double x;
    public final double y;

    /**
     * The {@code Point} constructor
     * @param x coordinate
     * @param y coordinate
     */
    public Point(double x, double y)
    {
        this.x = x;
        this.y = y;
    }
}
